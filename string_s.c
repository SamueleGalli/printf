#include "ft_printf.h"

int	string_s(char *c)
{
	int	i;

	i = 0;
	if (c == NULL)
	{
		return (0);
	}
	while (c[i] != '\0')
	{
		i++;
		character_c(c[i]);
	}
	return (i);
}
